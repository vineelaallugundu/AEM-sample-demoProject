package com.dish.wholesale.core.beans;

/**
 * The Class ListItems.
 */
public class ListItems {

	/** The list title. */
	private String listTitle;

	/** The list sub text. */
	private String listSubText;

	/** The list button text. */
	private String listButtonText;

	/** The list button link. */
	private String listButtonLink;

	/**
	 * Gets the list title.
	 *
	 * @return the list title
	 */
	public String getListTitle() {
		return listTitle;
	}

	/**
	 * Sets the list title.
	 *
	 * @param listTitle the new list title
	 */
	public void setListTitle(String listTitle) {
		this.listTitle = listTitle;
	}

	/**
	 * Gets the list sub text.
	 *
	 * @return the list sub text
	 */
	public String getListSubText() {
		return listSubText;
	}

	/**
	 * Sets the list sub text.
	 *
	 * @param listSubText the new list sub text
	 */
	public void setListSubText(String listSubText) {
		this.listSubText = listSubText;
	}

	/**
	 * Gets the list button text.
	 *
	 * @return the list button text
	 */
	public String getListButtonText() {
		return listButtonText;
	}

	/**
	 * Sets the list button text.
	 *
	 * @param listButtonText the new list button text
	 */
	public void setListButtonText(String listButtonText) {
		this.listButtonText = listButtonText;
	}

	/**
	 * Gets the list button link.
	 *
	 * @return the list button link
	 */
	public String getListButtonLink() {
		return listButtonLink;
	}

	/**
	 * Sets the list button link.
	 *
	 * @param listButtonLink the new list button link
	 */
	public void setListButtonLink(String listButtonLink) {
		this.listButtonLink = listButtonLink;
	}

}
