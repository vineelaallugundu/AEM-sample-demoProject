package com.dish.wholesale.core.beans;

/**
 * The Class HeaderItems.
 */
public class HeaderItems {

	/** The page label. */
	private String pageLabel;
	
	/** The page url. */
	private String pageUrl;

	/**
	 * Gets the page label.
	 *
	 * @return the page label
	 */
	public String getPageLabel() {
		return pageLabel;
	}

	/**
	 * Sets the page label.
	 *
	 * @param pageLabel the new page label
	 */
	public void setPageLabel(String pageLabel) {
		this.pageLabel = pageLabel;
	}

	/**
	 * Gets the page url.
	 *
	 * @return the page url
	 */
	public String getPageUrl() {
		return pageUrl;
	}

	/**
	 * Sets the page url.
	 *
	 * @param pageUrl the new page url
	 */
	public void setPageUrl(String pageUrl) {
		this.pageUrl = pageUrl;
	}

}
